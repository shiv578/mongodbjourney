db.createView("orderView","employees",
    [
    {$lookup:{
        from:"orders",
        localField:"_id",
        foreignField:"empid",
        as:"orders"
    }},
    {$unwind:"$orders"},
    {$project:{
        _id:0,
        name:1,
        email:1,
        department:1,
        product:"$orders.product",
        price:"$orders.price"
    }}
])